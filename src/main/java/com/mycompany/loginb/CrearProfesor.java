    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    String nombre = jTextField4.getText();
    String apellido = jTextField5.getText();
    String usuario = jTextField6.getText();
    String password = jTextField1.getText();
    
    String regex = "^(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z])(?=.*[@#$%^&+=!])(?=\\S+$).{8,}$";
    
    if (password.matches(regex)) {
        Profesor p = new Profesor();
        p.nombre = nombre;
        p.apellido = apellido;
        p.usuario = usuario;
        p.password = password;
        
        LoginB.profesores.add(p);
        
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField1.setText("");
        
        JOptionPane.showMessageDialog(this, "El Profesor fue creado exitosamente");
    } else {
        JOptionPane.showMessageDialog(this, "La contraseña debe contener al menos una mayúscula, una minúscula, un número y un carácter especial, y tener al menos 8 caracteres.",
                                      "Error de contraseña", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

  

